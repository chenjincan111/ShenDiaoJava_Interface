package fthink.entity;

public class NetworkChangeValue {

	private String noChange;
	private String networkChange;
	private String wifiNameChange;
	
	public String getNoChange() {
		return noChange;
	}
	public void setNoChange(String noChange) {
		this.noChange = noChange;
	}
	public String getNetworkChange() {
		return networkChange;
	}
	public void setNetworkChange(String networkChange) {
		this.networkChange = networkChange;
	}
	public String getWifiNameChange() {
		return wifiNameChange;
	}
	public void setWifiNameChange(String wifiNameChange) {
		this.wifiNameChange = wifiNameChange;
	}
	
	
}
